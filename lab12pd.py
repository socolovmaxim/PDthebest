# Шаг 1: Запрос пользователя на ввод числа от 1 до 9.
x = int(input("Введите число от 1 до 9: "))

# Шаг 2: Если x от 1 до 3, включительно.
if 1 <= x <= 3:
    # Попросить пользователя ввести строку и количество повторений.
    s = input("Введите строку: ")
    n = int(input("Введите количество повторений строки: "))
    
    # Выполнить цикл повторения строки n раз и вывести результат.
    for i in range(n):
        print(s)

# Шаг 3: Если x от 4 до 6, включительно.
elif 4 <= x <= 6:
    # Попросить пользователя ввести степень, в которую нужно возвести число x.
    m = int(input("Введите степень для возведения числа: "))
    
    # Возвести x в степень m и вывести результат.
    result = x ** m
    print(f"{x} в степени {m} равно {result}")

# Шаг 4: Если x от 7 до 9, включительно.
elif 7 <= x <= 9:
    # Выполнить увеличение числа x на единицу в цикле 10 раз и выводить все числа.
    for i in range(1, 11):
        x += 1
        print(x)

# Шаг 5: Во всех остальных случаях выводить сообщение об ошибке ввода.
else:
    print("Ошибка ввода")
